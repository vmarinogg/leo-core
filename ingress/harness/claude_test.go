func TestBackupCreatedForUserFile(t *testing.T) {
	dir := t.TempDir()
	userFile := filepath.Join(dir, "CLAUDE.md")
	os.WriteFile(userFile, []byte("# My custom rules"), 0644)

	backed, err := BackupIfNeeded(userFile)
	if err != nil {
		t.Fatalf("BackupIfNeeded failed: %v", err)
	}
	if !backed {
		t.Error("expected backup to be created for user file")
	}

	bkpContent, err := os.ReadFile(userFile + ".bkp")
	if err != nil {
		t.Fatal("backup file not created")
	}
	if string(bkpContent) != "# My custom rules" {
		t.Error("backup content doesn't match original")
	}
}

