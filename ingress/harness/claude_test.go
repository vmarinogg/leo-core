func TestClaudeAdapter_GenerateContextFile_CommunicationModes(t *testing.T) {
	modes := []struct {
		mode string
		want string
	}{
		{"concise", "## Communication mode: Concise"},
		{"efficient", "## Communication mode: Efficient"},
	}

	for _, tc := range modes {
		t.Run(tc.mode, func(t *testing.T) {
			dir := t.TempDir()
			a := NewClaudeAdapter(dir)
			cfg := Config{
				Version:  "1",
				Delivery: "context-file",
				User:     UserConfig{Language: "en", Autonomy: "balanced", CommunicationMode: tc.mode},
			}
			if err := a.GenerateContextFile(cfg, nil, nil, nil); err != nil {
				t.Fatalf("GenerateContextFile failed: %v", err)
			}
			content, _ := os.ReadFile(filepath.Join(dir, ".claude", "CLAUDE.md"))
			if !strings.Contains(string(content), tc.want) {
				t.Errorf("CLAUDE.md missing %q for mode %q", tc.want, tc.mode)
			}
		})
	}

	// "default" mode returns empty instructions — no communication header in output.
	t.Run("default", func(t *testing.T) {
		dir := t.TempDir()
		a := NewClaudeAdapter(dir)
		cfg := Config{
			Version:  "1",
			Delivery: "context-file",
			User:     UserConfig{Language: "en", Autonomy: "balanced", CommunicationMode: "default"},
		}
		if err := a.GenerateContextFile(cfg, nil, nil, nil); err != nil {
			t.Fatalf("GenerateContextFile failed: %v", err)
		}
		content, _ := os.ReadFile(filepath.Join(dir, ".claude", "CLAUDE.md"))
		if strings.Contains(string(content), "## Communication mode") {
			t.Error("CLAUDE.md should not contain communication mode header for default mode")
		}
	})
}

func TestClaudeAdapter_RegisterHooks(t *testing.T) {
	dir := t.TempDir()
	a := NewClaudeAdapter(dir)

	if err := a.RegisterHooks(); err != nil {
		t.Fatalf("RegisterHooks failed: %v", err)
	}

	content, err := os.ReadFile(filepath.Join(dir, ".claude", "settings.json"))
	if err != nil {
		t.Fatalf("reading settings.json: %v", err)
	}

	var settings map[string]any
	if err := json.Unmarshal(content, &settings); err != nil {
		t.Fatalf("parsing settings.json: %v", err)
	}

	hooksMap, ok := settings["hooks"].(map[string]any)
	if !ok {
		t.Fatal("hooks should be a map")
	}

	stop, ok := hooksMap["Stop"].([]any)
	if !ok {
		t.Fatal("hooks.Stop should be an array")
	}
	if len(stop) != 1 {
		t.Fatalf("expected 1 Stop matcher group (sweep only), got %d", len(stop))
	}

	group0 := stop[0].(map[string]any)
	innerHooks0 := group0["hooks"].([]any)
	hookEntry0 := innerHooks0[0].(map[string]any)
	if hookEntry0["command"] != "mom watch --sweep" {
		t.Errorf("expected command 'mom watch --sweep', got %v", hookEntry0["command"])
	}

	// SessionEnd has a fallback sweep to catch the last response.
	sessionEnd, ok := hooksMap["SessionEnd"].([]any)
	if !ok {
		t.Fatal("hooks.SessionEnd should be an array")
	}
	if len(sessionEnd) != 1 {
		t.Fatalf("expected 1 SessionEnd matcher group (sweep), got %d", len(sessionEnd))
	}
	seGroup := sessionEnd[0].(map[string]any)
	seHooks := seGroup["hooks"].([]any)
	seEntry := seHooks[0].(map[string]any)
	if seEntry["command"] != "mom watch --sweep" {
		t.Errorf("expected SessionEnd command 'mom watch --sweep', got %v", seEntry["command"])
	}
}

func TestClaudeAdapter_RegisterMCP_Fresh(t *testing.T) {
	dir := t.TempDir()
	a := NewClaudeAdapter(dir)

	if err := a.RegisterMCP(); err != nil {
		t.Fatalf("RegisterMCP failed: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(dir, ".mcp.json"))
	if err != nil {
		t.Fatalf("reading .mcp.json: %v", err)
	}
	var root map[string]any
	if err := json.Unmarshal(data, &root); err != nil {
		t.Fatalf("parsing .mcp.json: %v", err)
	}
	servers, ok := root["mcpServers"].(map[string]any)
	if !ok {
		t.Fatal(".mcp.json missing mcpServers")
	}
	mom, ok := servers["mom"].(map[string]any)
	if !ok {
		t.Fatal(".mcp.json missing mom server entry")
	}
	cmd, _ := mom["command"].(string)
	if cmd == "" {
		t.Error(".mcp.json mom command must not be empty")
	}
	s := string(data)
	if !strings.Contains(s, `"serve"`) {
		t.Error(".mcp.json missing serve arg")
	}
}

func TestClaudeAdapter_RegisterMCP_PreservesExisting(t *testing.T) {
	dir := t.TempDir()
	a := NewClaudeAdapter(dir)

	// Write an existing .mcp.json with another server.
	existing := `{"mcpServers": {"other": {"command": "other-tool", "args": ["run"]}}}` + "\n"
	os.WriteFile(filepath.Join(dir, ".mcp.json"), []byte(existing), 0644)

	if err := a.RegisterMCP(); err != nil {
		t.Fatalf("RegisterMCP failed: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(dir, ".mcp.json"))
	if err != nil {
		t.Fatalf("reading .mcp.json: %v", err)
	}
	s := string(data)
	if !strings.Contains(s, `"other"`) {
		t.Error(".mcp.json lost existing server entry")
	}
	if !strings.Contains(s, `"mom"`) {
		t.Error(".mcp.json missing mom server entry")
	}
}

