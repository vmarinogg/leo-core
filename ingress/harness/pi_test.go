func TestPiAdapter_RegisterMCP_FreshFile(t *testing.T) {
	dir := t.TempDir()
	a := NewPiAdapter(dir)

	if err := a.RegisterMCP(); err != nil {
		t.Fatalf("RegisterMCP: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(dir, ".mcp.json"))
	if err != nil {
		t.Fatalf("reading .mcp.json: %v", err)
	}
	var root map[string]any
	if err := json.Unmarshal(data, &root); err != nil {
		t.Fatalf("parsing .mcp.json: %v", err)
	}
	servers, ok := root["mcpServers"].(map[string]any)
	if !ok {
		t.Fatal(".mcp.json missing mcpServers")
	}
	mom, ok := servers["mom"].(map[string]any)
	if !ok {
		t.Fatal(".mcp.json missing mom entry")
	}
	if cmd, _ := mom["command"].(string); cmd == "" {
		t.Error("mom.command must not be empty")
	}
	if env, ok := mom["env"].(map[string]any); !ok {
		t.Error("mom.env missing")
	} else if env["MOM_PROJECT_DIR"] != dir {
		t.Errorf("MOM_PROJECT_DIR: got %v, want %s", env["MOM_PROJECT_DIR"], dir)
	}
	if !strings.Contains(string(data), `"serve"`) {
		t.Error(".mcp.json missing serve arg")
	}
}

func TestPiAdapter_RegisterMCP_PreservesOtherServers(t *testing.T) {
	dir := t.TempDir()
	mcpPath := filepath.Join(dir, ".mcp.json")

	// Pre-existing .mcp.json with another server entry.
	pre := map[string]any{
		"mcpServers": map[string]any{
			"other": map[string]any{
				"command": "/usr/bin/other",
				"args":    []any{"--foo"},
			},
		},
	}
	preData, _ := json.MarshalIndent(pre, "", "  ")
	if err := os.WriteFile(mcpPath, preData, 0644); err != nil {
		t.Fatal(err)
	}

	a := NewPiAdapter(dir)
	if err := a.RegisterMCP(); err != nil {
		t.Fatalf("RegisterMCP: %v", err)
	}

	data, _ := os.ReadFile(mcpPath)
	var root map[string]any
	if err := json.Unmarshal(data, &root); err != nil {
		t.Fatalf("parsing .mcp.json: %v", err)
	}
	servers := root["mcpServers"].(map[string]any)
	if _, ok := servers["other"]; !ok {
		t.Error("other server entry was clobbered by RegisterMCP")
	}
	if _, ok := servers["mom"]; !ok {
		t.Error("mom server entry was not added")
	}
}

func TestPiAdapter_DetectHarness(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)
	t.Setenv("PATH", t.TempDir())
	a := NewPiAdapter(t.TempDir())

	if a.DetectHarness() {
		t.Error("expected false when global pi config absent")
	}

	if err := os.MkdirAll(filepath.Join(home, ".pi", "agent"), 0755); err != nil {
		t.Fatal(err)
	}
	if !a.DetectHarness() {
		t.Error("expected true when global pi config exists")
	}
}

func TestPiAdapter_DetectHarness_DoesNotMatchOnAGENTSMd(t *testing.T) {
	t.Setenv("HOME", t.TempDir())
	t.Setenv("PATH", t.TempDir())
	// Codex also writes AGENTS.md. Pi must NOT report itself as detected
	// based on AGENTS.md alone, otherwise every Codex project would
	// spuriously claim pi.
	dir := t.TempDir()
	if err := os.WriteFile(filepath.Join(dir, "AGENTS.md"), []byte("..."), 0644); err != nil {
		t.Fatal(err)
	}
	if NewPiAdapter(dir).DetectHarness() {
		t.Error("AGENTS.md alone must not trigger pi detection")
	}
}

func TestPiAdapter_GeneratedFiles(t *testing.T) {
	a := NewPiAdapter("/tmp/x")
	got := a.GeneratedFiles()
	// The pi-mom extension is installed via the Pi marketplace into
	// ~/.pi/agent/extensions/ (see RegisterGlobalExtension) — it is
	// not a MOM-generated project file. Only AGENTS.md and .mcp.json
	// land in the project root.
	want := []string{
		"AGENTS.md",
		".mcp.json",
	}
	if len(got) != len(want) {
		t.Fatalf("len: got %d, want %d", len(got), len(want))
	}
	for i := range want {
		if got[i] != want[i] {
			t.Errorf("file[%d]: got %q, want %q", i, got[i], want[i])
		}
	}
}

