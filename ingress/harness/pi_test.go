func TestPiAdapter_GenerateContextFile(t *testing.T) {
	dir := t.TempDir()
	a := NewPiAdapter(dir)

	cfg := Config{
		Version:  "1",
		Delivery: "context-file",
		User: UserConfig{
			Language:          "en",
			Autonomy:          "balanced",
			CommunicationMode: "concise",
		},
	}
	constraints := []Constraint{
		{ID: "anti-hallucination", Summary: "When unsure, say you don't know."},
	}
	skills := []Skill{
		{ID: "session-wrap-up", Summary: "End-of-session knowledge propagation."},
	}
	identity := &Identity{What: "MOM — a living knowledge infrastructure."}

	if err := a.GenerateContextFile(cfg, constraints, skills, identity); err != nil {
		t.Fatalf("GenerateContextFile: %v", err)
	}

	content, err := os.ReadFile(filepath.Join(dir, "AGENTS.md"))
	if err != nil {
		t.Fatalf("reading AGENTS.md: %v", err)
	}
	s := string(content)
	if !strings.Contains(s, "Generated by MOM") {
		t.Error("AGENTS.md missing watermark")
	}
	if !strings.Contains(s, "anti-hallucination") {
		t.Error("AGENTS.md missing constraint")
	}
	if !strings.Contains(s, "session-wrap-up") {
		t.Error("AGENTS.md missing skill")
	}
}

