// ── Test 6: MCP config correctness ──────────────────────────────────────────

func TestIntegration_MCPConfigCorrectness(t *testing.T) {
	dir := t.TempDir()
	home := t.TempDir()
	t.Setenv("HOME", home)

	allHarnesses := []string{"claude", "codex", "windsurf"}
	initProject(t, dir, allHarnesses)

	mcpPath := filepath.Join(home, ".codeium", "windsurf", "mcp_config.json")

	// Verify structure.
	m := readJSONFile(t, mcpPath)
	servers, ok := m["mcpServers"].(map[string]any)
	if !ok {
		t.Fatal(".mcp.json missing mcpServers")
	}

	mom, ok := servers["mom"].(map[string]any)
	if !ok {
		t.Fatal(".mcp.json missing mcpServers.mom")
	}

	cmd, _ := mom["command"].(string)
	if cmd == "" {
		t.Errorf("mcpServers.mom.command must not be empty, got %v", mom["command"])
	}

	args, ok := mom["args"].([]any)
	if !ok || len(args) < 2 {
		t.Errorf("mcpServers.mom.args missing or too short: %v", mom["args"])
	}

	if _, ok := mom["env"]; ok {
		t.Error("global mcpServers.mom should not include project-specific env")
	}
}

// ── Test 7: Upgrade preserves custom MCP entries ────────────────────────────

func TestIntegration_UpgradePreservesCustomMCP(t *testing.T) {
	dir := t.TempDir()
	home := t.TempDir()
	t.Setenv("HOME", home)

	initProject(t, dir, []string{"claude", "windsurf"})

	mcpPath := filepath.Join(home, ".codeium", "windsurf", "mcp_config.json")

	// Add a custom MCP server entry alongside mom.
	m := readJSONFile(t, mcpPath)
	servers := m["mcpServers"].(map[string]any)
	servers["my-custom-server"] = map[string]any{
		"command": "my-server",
		"args":    []any{"start"},
	}
	m["mcpServers"] = servers

	data, _ := json.MarshalIndent(m, "", "  ")
	data = append(data, '\n')
	os.WriteFile(mcpPath, data, 0644)

	// Verify custom entry exists before upgrade.
	pre := readJSONFile(t, mcpPath)
	preSrv := pre["mcpServers"].(map[string]any)
	if _, ok := preSrv["my-custom-server"]; !ok {
		t.Fatal("custom server not written to .mcp.json")
	}

	// Re-run init.
	initProject(t, dir, []string{"claude", "windsurf"})

	// Verify both mom and custom server survive.
	post := readJSONFile(t, mcpPath)
	postSrv := post["mcpServers"].(map[string]any)

	if _, ok := postSrv["mom"]; !ok {
		t.Error("mcpServers.mom missing after upgrade")
	}
	if _, ok := postSrv["my-custom-server"]; !ok {
		t.Error("mcpServers.my-custom-server lost after upgrade — custom entries not preserved")
	}

	// Verify custom server content is intact.
	custom := postSrv["my-custom-server"].(map[string]any)
	if custom["command"] != "my-server" {
		t.Errorf("custom server command = %v, want my-server", custom["command"])
	}
}
